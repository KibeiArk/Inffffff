'''Задание №9(6254)
 
(PRO100 ЕГЭ) Два игрока, Петя и Ваня, играют в следующую игру. Перед игроками лежит куча
 камней. Игроки ходят по очереди, первый ход делает Петя. За один ход игрок может добавить в кучу
 один камень или три камня или одиннадцать камней. Для того чтобы делать ходы, у каждого игрока
 есть неограниченное количество камней.
 Игра завершается в тот момент, когда количество камней в куче становится числом,
 оканчивающимся на ноль. Победителем считается игрок, сделавший последний ход, т.е. первым
 получивший кучу, количество камней в которой оканчивается на ноль. К примеру, игра
 заканчивается, когда в куче стало 10, 200, 6800 камней. В начальный момент в куче было S камней,
 где S – двузначное число, не оканчивающиеся на ноль.
 Ответьте на следующие вопросы:
 Вопрос 1. Укажите минимальное значение S, при котором Петя не может выиграть за один ход, но
 при любом ходе Пети Ваня может выиграть своим первым ходом.
 Вопрос 2. Найдите количество значений S, при которых у Пети есть выигрышная стратегия, причём
 одновременно выполняются два условия:
 – Петя не может выиграть за один ход;
 – Петя может выиграть своим вторым ходом независимо от того, как будет ходить Ваня.
 Вопрос 3. Найдите сумму значений S, при которых у Вани есть выигрышная стратегия, причём
 одновременно выполняются два условия:
 — у Вани есть выигрышная стратегия, позволяющая ему выиграть первым или вторым ходом при
 любой игре Пети;

 — у Вани нет стратегии, которая позволит ему гарантированно выиграть первым ходом'''
def f(s,m):
    if s%10==0:return m%2==0
    if m==0:return 0
    h = [f(s+1,m-1),f(s+3,m-1),f(s+11,m-1)]
    return any(h) if (m-1)%2==0 else all(h)
print(19,[s for s in range(10,100) if s %10!=0 and f(s,2)])#16
print(20,len([s for s in range(10,100) if s %10!=0 and not f(s,1) and f(s,3)]))#1
print(21,sum([s for s in range(10,100) if s %10!=0 and not f(s,2) and f(s,4)]))
